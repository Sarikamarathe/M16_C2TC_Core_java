package com.cg.git;

public class multiarray {

	public static void main(String[] args) {
		int num=1;
		int a[][]=new int[3][4];
		for(int i=0;i<3;i++) {
			for(int j=0;j<4;j++) {
				a[i][j]=num;
				System.out.print(a[i][j]+" ");
				num++;

			}
			System.out.println();

			
				

			
		}

	}

}
